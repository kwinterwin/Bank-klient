    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int i = jComboBox1.getSelectedIndex();
        

        String surname = "";
        String name = "";
        String lastname = "";
        String passport = "";
        String account = "";
        float summa = 0.0f;
        String currency = "";
        int account_type = 0;
        
        Date date_open = new Date();
        Date date_close = new Date();
        
        int month = date_open.getMonth();
        int close = month+period[i];
        date_close.setMonth(close);
 

        SimpleDateFormat formatForDateNow = new SimpleDateFormat("yyyy.MM.dd");

        String data_open = formatForDateNow.format(date_open);
        String data_close = formatForDateNow.format(date_close);
        
        try {
            String clientMessage = "";
           
            surname = jTextField1.getText();
            name = jTextField2.getText();
            lastname = jTextField3.getText();
            passport = jTextField6.getText();
            account = jTextField5.getText();
            summa = (float) jSpinner1.getValue();
            currency = jTextField4.getText();

            clientMessage = "openDeposit" + ","  + account + "," + data_open + "," + data_close + "," + summa + ","  
                    + currency + "," + account_type + "," +  surname + "," + name + "," 
                            + lastname + "," + passport;

            Authorization.os.writeObject(clientMessage);
            
            String message = (String) Authorization.is.readObject();
            
            if (message.equals("success")) {
                   jLabel9.setForeground(Color.red);
                   jLabel9.setText("Депозит успешно открыт!");
                   
            }
            if (message.equals("fail")) {
               jLabel9.setForeground(Color.red);
               jLabel9.setText("Ошибка при открытии депозита!");
            }
        } catch (Exception e) {
            
        }
    }//GEN-LAST:event_jButton1ActionPerformed

