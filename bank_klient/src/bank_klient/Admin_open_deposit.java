    public void add(){

        try {
            String clientMessage = "";
            int count = 0;                               
            clientMessage = "allDeposit";

            Authorization.os.writeObject(clientMessage);
            
            String message = (String) Authorization.is.readObject();
            
            String mesParts[] = message.split(",");
            
            if (mesParts[0].equals("success")) {
                count = (mesParts.length-1)/4;
                deposit = new String[count];
                currency = new String[count];
                sum = new float[count];
                period = new int[count];
                
                int j=0;
                for(int i=1;i<mesParts.length;i=i+4){
                   deposit[j] = mesParts[i];
                   j++;   
                }  
                j=0;
                for(int i=2;i<mesParts.length;i=i+4){
                   currency[j] = mesParts[i];
                   j++;   
                }
                j=0;
                for(int i=3;i<mesParts.length;i=i+4){
                   sum[j] = Float.parseFloat(mesParts[i]);
                   j++;   
                }
                j=0;
                for(int i=4;i<mesParts.length;i=i+4){
                   period[j] = Integer.parseInt(mesParts[i]);
                   j++;   
                }
                
            }
            else {
                deposit = new String[count];
                currency = new String[count];
                sum = new float[count];
                period = new int[count];
            }
        } catch (Exception e) {
            
        }      
    }
